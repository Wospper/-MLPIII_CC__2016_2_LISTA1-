	 private double precoDeLocacao;
	 private String categoriaDoFilme;
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public double getPrecoDeLocacao() {
		return precoDeLocacao;
	}
